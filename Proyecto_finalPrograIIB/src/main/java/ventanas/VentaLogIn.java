    private void validar(){
        int resultado = 0;
        try{
            String user = txt_username.getText();
            String pass = String.valueOf(txt_pass.getPassword());
            String query = "select * from test.usuarios where usuario = '"+user+"' and pwd = '"+pass+"'";
            Statement stmt = con.createStatement();
            rs = stmt.executeQuery(query);
            if (rs.next()) {
                resultado = 1;
                if (resultado == 1) {
                    ventanaPokedex ventana = new ventanaPokedex();
                    ventana.setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Usuario o contrase√±a incorrectos");
                }
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Algo ha salido mal en el acceso" + e.getMessage());
        }
    }

